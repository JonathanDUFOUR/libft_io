#include <stdlib.h>
#include "ft_io.h"

char	*ft_llutoa(t_lluint nb)
{
	char	*output;
	char	*ptr;
	t_uint	len;

	len = ft_lluintlen(nb);
	output = malloc((len + 1) * sizeof(char));
	if (!output)
		return (NULL);
	ptr = output + len;
	*ptr-- = 0;
	if (!nb)
		*ptr = '0';
	else
	{
		while (nb && len--)
		{
			*ptr-- = (nb % 10) + '0';
			nb /= 10;
		}
	}
	return (output);
}
